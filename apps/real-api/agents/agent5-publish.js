/**
 * Agent 5: GitHub Publisher + Azure Container Apps Deployer
 *
 * Creates a real GitHub repo, commits all generated code,
 * and deploys as an ephemeral Azure Container App.
 */

import { Octokit } from '@octokit/rest';
import { execSync } from 'child_process';

const GITHUB_TOKEN = process.env.GITHUB_TOKEN;
const GITHUB_ORG = process.env.GITHUB_ORG || '';
const ACR_LOGIN_SERVER = process.env.ACR_LOGIN_SERVER;
const ACR_USERNAME = process.env.ACR_USERNAME;
const ACR_PASSWORD = process.env.ACR_PASSWORD;
const ACA_RESOURCE_GROUP = process.env.ACA_RESOURCE_GROUP;
const ACA_ENVIRONMENT = process.env.ACA_ENVIRONMENT;

function generateDockerfile(hasReact, hasVue) {
  const stages = [];

  if (hasReact) {
    stages.push(`FROM node:20-alpine AS build-react
WORKDIR /app/react
COPY react/package*.json ./
RUN npm install
COPY react/ ./
RUN npm run build`);
  }

  if (hasVue) {
    stages.push(`FROM node:20-alpine AS build-vue
WORKDIR /app/vue
COPY vue/package*.json ./
RUN npm install
COPY vue/ ./
RUN npm run build`);
  }

  let copies = '';
  if (hasReact) copies += '\nCOPY --from=build-react /app/react/dist /usr/share/nginx/html/react';
  if (hasVue) copies += '\nCOPY --from=build-vue /app/vue/dist /usr/share/nginx/html/vue';

  return `${stages.join('\n\n')}

FROM nginx:alpine
RUN echo 'server { listen 80; root /usr/share/nginx/html; index index.html; location /react/ { try_files $uri $uri/ /react/index.html; } location /vue/ { try_files $uri $uri/ /vue/index.html; } location /docs/ { autoindex on; } location / { try_files $uri /index.html; } }' > /etc/nginx/conf.d/default.conf${copies}
COPY docs/ /usr/share/nginx/html/docs/
COPY public/index.html /usr/share/nginx/html/index.html
EXPOSE 80`;
}

